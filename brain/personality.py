def get_dynamic_greeting(driver_name='капитан'):
    """Возвращает случайное приветствие в зависимости от времени суток."""
    # Тут можно будет добавить логику определения времени суток
    # А пока просто смешаем все варианты
    all_greetings = GREETINGS['morning'] + GREETINGS['day'] + GREETINGS['evening'] + GREETINGS['special']
    chosen_greeting = random.choice(all_greetings)
    return chosen_greeting.format(driver_name=driver_name)