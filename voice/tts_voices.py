# и легко добавлять новые голоса в будущем.

AVAILABLE_RU_SPEAKERS = {
    # Женские голоса
    'baya': 'стандартный женский',
    'kseniya': 'мягкий женский',
    'xenia': 'нейтральный женский',
    # Мужские голоса
    'aidar': 'стандартный мужской',
    'eugene': 'низкий мужской'
}

def get_valid_speakers():
    """Возвращает список имен спикеров, которые можно использовать в командах."""
    return list(AVAILABLE_RU_SPEAKERS.keys())